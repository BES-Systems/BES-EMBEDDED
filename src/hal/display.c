static void send_cmd(uint8_t cmd)
{
    esp_lcd_panel_io_tx_param(
        s_io_handle,
        cmd,
        NULL,
        0
    );
}

static void send_param(const void *data, size_t len)
{
    esp_lcd_panel_io_tx_param(
        s_io_handle,
        0,
        data,
        len
    );
}
static void send_color(const void *data, size_t len)
{
    esp_lcd_panel_io_tx_color(s_io_handle, CMD_RAMWR, data, len);
}

static void ili9341_init_sequence(void)
{
    const uint8_t *p = ILI9341_INIT;
    size_t remaining = sizeof(ILI9341_INIT);

    while (remaining > 0)
    {
        uint8_t cmd = *p++;
        uint8_t argc = *p++;

        remaining -= 2;

        if(cmd == 0x00 && (argc & 0x80))
        {
            vTaskDelay(
                pdMS_TO_TICKS(
                    (argc & 0x7F) * 120
                )
            );

            continue;
        }


        esp_lcd_panel_io_tx_param(
            s_io_handle,
            cmd,
            p,
            argc
        );

        p += argc;
        remaining -= argc;
    }


    // MADCTL landscape
    send_cmd(0x36);

    uint8_t madctl = 0x28;

    send_param(
        &madctl,
        1
    );


    // RGB565
    send_cmd(0x3A);

    uint8_t pixel = 0x55;

    send_param(
        &pixel,
        1
    );


    vTaskDelay(
        pdMS_TO_TICKS(100)
    );
}

static inline uint16_t color565(uint8_t r, uint8_t g, uint8_t b)
{
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

void bes_display_init(void)
{
    bes_gpio_output(PIN_NUM_BCKL);

    gpio_config_t rst_conf = {
        .pin_bit_mask = (1ULL << PIN_NUM_RST),
        .mode = GPIO_MODE_OUTPUT,
    };

    gpio_config(&rst_conf);

    gpio_set_level(PIN_NUM_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(20));

    gpio_set_level(PIN_NUM_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(150));

    spi_bus_config_t bus_cfg = {
        .mosi_io_num = PIN_NUM_MOSI,
        .miso_io_num = -1,
        .sclk_io_num = PIN_NUM_SCLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = DISPLAY_WIDTH * DISPLAY_HEIGHT * 2,
    };
    spi_bus_initialize(SPI_HOST_ID, &bus_cfg, SPI_DMA_CH_AUTO);

    esp_lcd_panel_io_spi_config_t io_cfg = {
        .cs_gpio_num = PIN_NUM_CS,
        .dc_gpio_num = PIN_NUM_DC,
        .spi_mode = 0,
        .pclk_hz = 40 * 1000 * 1000,
        .trans_queue_depth = 10,
        .lcd_cmd_bits = 8,
        .lcd_param_bits = 8,
    };
    esp_lcd_new_panel_io_spi(SPI_HOST_ID, &io_cfg, &s_io_handle);

    ili9341_init_sequence();

    s_framebuf = heap_caps_malloc(DISPLAY_WIDTH * DISPLAY_HEIGHT * 2, MALLOC_CAP_SPIRAM | MALLOC_CAP_8BIT);
    if (!s_framebuf) {
        s_framebuf = heap_caps_malloc(DISPLAY_WIDTH * DISPLAY_HEIGHT * 2, MALLOC_CAP_8BIT);
    }

    bes_display_on();
    bes_log("Display initialized");

    bes_device_register(
        "display",
        NULL
    );
}

void bes_display_on(void)
{
    bes_gpio_write(PIN_NUM_BCKL, 1);
    send_cmd(CMD_DISPON);
}

void bes_display_off(void)
{
    send_cmd(0x28);
    bes_gpio_write(PIN_NUM_BCKL, 0);
}

void bes_display_fill(uint16_t color)
{
    for (int i = 0; i < DISPLAY_WIDTH * DISPLAY_HEIGHT; i++) {
        s_framebuf[i] = color;
    }

    uint8_t caset[4] = { 0, 0, (uint8_t)((DISPLAY_WIDTH - 1) >> 8), (uint8_t)(DISPLAY_WIDTH - 1) };
    send_cmd(0x2A);
    send_param(caset, 4);
    uint8_t raset[4] = { 0, 0, (uint8_t)((DISPLAY_HEIGHT - 1) >> 8), (uint8_t)(DISPLAY_HEIGHT - 1) };
    send_cmd(0x2B);
    send_param(raset, 4);

    send_color(s_framebuf, DISPLAY_WIDTH * DISPLAY_HEIGHT * 2);
}

